class CssPropertyConverter {
public:
	string getCamelized(string cssPropertyName) {
        string tmp = "";
        string ret = "";
        for( int i = 0; i < cssPropertyName.size(); i++ )
        {
            if( cssPropertyName[i] == '-' )
            {
                if( ret == "" )
                {
                    for( int i = 0; i <= tmp.size(); i++ )
                        ret += tmp[i];
                }
                else
                {
                    ret += toupper( tmp[0] );
                    for( int i = 1; i <= tmp.size(); i++ )
                        ret += tmp[i];
                }
                tmp = "";
            }
            else
                tmp += cssPropertyName[i];
        }

        ret += toupper( tmp[0] );
        for( int i = 1; i <= tmp.size(); i++ )
            ret += tmp[i];

        return ret;
	}
};


