class Time {
public:
	string whatTime(int seconds) {
		int a = seconds / 3600;
		int b = (seconds - (a*3600))/60;
		int c = seconds - (a*3600) - b*60;
		
		char buffer[10];
		
		sprintf(buffer, "%d:%d:%d", a, b, c); 

		return buffer;
		
	}
};


